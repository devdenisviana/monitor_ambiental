// Envia um comando para o SSD1306
static void ssd1306_send_cmd(ssd1306_t *display, uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(display->i2c_port, display->address, buf, 2, false);
}

// Inicializa o display SSD1306
void ssd1306_init(ssd1306_t *display, i2c_inst_t *i2c_port, uint8_t address) {
    display->i2c_port = i2c_port;
    display->address = address;
    display->width = SSD1306_WIDTH;
    display->height = SSD1306_HEIGHT;
    
    // Sequência de inicialização
    ssd1306_send_cmd(display, SSD1306_SET_DISP | 0x00); // Display OFF
    ssd1306_send_cmd(display, SSD1306_SET_MEM_ADDR);    // Set Memory Addressing Mode
    ssd1306_send_cmd(display, 0x00);                    // Horizontal Addressing Mode
    ssd1306_send_cmd(display, SSD1306_SET_DISP_START_LINE | 0x00); // Set Display Start Line
    ssd1306_send_cmd(display, SSD1306_SET_SEG_REMAP | 0x01); // Set Segment Re-map
    ssd1306_send_cmd(display, SSD1306_SET_MUX_RATIO);  // Set Multiplex Ratio
    ssd1306_send_cmd(display, SSD1306_HEIGHT - 1);
    ssd1306_send_cmd(display, SSD1306_SET_COM_OUT_DIR | 0x08); // Set COM Output Scan Direction
    ssd1306_send_cmd(display, SSD1306_SET_DISP_OFFSET); // Set Display Offset
    ssd1306_send_cmd(display, 0x00);
    ssd1306_send_cmd(display, SSD1306_SET_COM_PIN_CFG); // Set COM Pins Hardware Configuration
    ssd1306_send_cmd(display, 0x12);
    ssd1306_send_cmd(display, SSD1306_SET_CONTRAST);    // Set Contrast Control
    ssd1306_send_cmd(display, 0xFF);
    ssd1306_send_cmd(display, SSD1306_SET_PRECHARGE);   // Set Pre-charge Period
    ssd1306_send_cmd(display, 0xF1);
    ssd1306_send_cmd(display, SSD1306_SET_VCOM_DESEL);  // Set VCOMH Deselect Level
    ssd1306_send_cmd(display, 0x40);
    ssd1306_send_cmd(display, SSD1306_SET_ENTIRE_ON);   // Set Entire Display ON
    ssd1306_send_cmd(display, SSD1306_SET_NORM_INV);    // Set Normal Display
    ssd1306_send_cmd(display, SSD1306_SET_CHARGE_PUMP); // Enable charge pump
    ssd1306_send_cmd(display, 0x14);
    ssd1306_send_cmd(display, SSD1306_SET_DISP | 0x01); // Display ON
    
    ssd1306_clear(display);
    ssd1306_show(display);
}

// Limpa o buffer do display
void ssd1306_clear(ssd1306_t *display) {
    memset(display->buffer, 0, sizeof(display->buffer));
}

// Atualiza o display com o conteúdo do buffer
void ssd1306_show(ssd1306_t *display) {
    ssd1306_send_cmd(display, SSD1306_SET_COL_ADDR);
    ssd1306_send_cmd(display, 0);
    ssd1306_send_cmd(display, SSD1306_WIDTH - 1);
    
    ssd1306_send_cmd(display, SSD1306_SET_PAGE_ADDR);
    ssd1306_send_cmd(display, 0);
    ssd1306_send_cmd(display, (SSD1306_HEIGHT / 8) - 1);
    
    // Envia o buffer
    for (int i = 0; i < sizeof(display->buffer); i += 16) {
        uint8_t buf[17];
        buf[0] = 0x40; // Co = 0, D/C = 1
        memcpy(buf + 1, display->buffer + i, 16);
        i2c_write_blocking(display->i2c_port, display->address, buf, 17, false);
    }
}

// Desenha um pixel no buffer
void ssd1306_draw_pixel(ssd1306_t *display, int16_t x, int16_t y, bool on) {
    if (x < 0 || x >= display->width || y < 0 || y >= display->height) {
        return;
    }
    
    if (on) {
        display->buffer[x + (y / 8) * display->width] |= (1 << (y % 8));
    } else {
        display->buffer[x + (y / 8) * display->width] &= ~(1 << (y % 8));
    }
}

// Desenha um caractere no buffer
void ssd1306_draw_char(ssd1306_t *display, int16_t x, int16_t y, char c) {
    if (c < 32 || c > 127) {
        c = 32; // Espaço se o caractere for inválido
    }
    
    const uint8_t *glyph = font[c - 32];
    
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 8; j++) {
            if (glyph[i] & (1 << j)) {
                ssd1306_draw_pixel(display, x + i, y + j, true);
            }
        }
    }
}

// Desenha uma string no buffer
void ssd1306_draw_string(ssd1306_t *display, int16_t x, int16_t y, const char *str) {
    int16_t cursor_x = x;
    
    while (*str) {
        ssd1306_draw_char(display, cursor_x, y, *str);
        cursor_x += 6; // 5 pixels de largura + 1 de espaçamento
        str++;
    }
}
